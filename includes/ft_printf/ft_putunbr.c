#include "ft_printf.h"
#include <stdio.h>
#include <stdarg.h>

int	ft_putunbr(unsigned int n)
{
	int	len;

	len = 0;
	if (n >= 10)
		len += ft_putunbr(n / 10);
	ft_putchar(n % 10 + '0');
	len++;
	return (len);
}
